package co.nytro.market.datastores.dynamodb;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import java.io.Serializable;
import java.util.HashMap;
import java.util.Map;

@DynamoDBTable(tableName = "MarketListings")
public class DynamoDBListing implements Serializable {

    private String itemStack;
    private int price;
    private int quantity;
    private int stock;
    private String seller;
    private String id;
    private boolean server;

    @DynamoDBHashKey(attributeName = "ID")
    @DynamoDBAutoGeneratedKey
    public String getID() {
        return id;
    }

    public void setID(String id) {
        this.id = id;
    }

    @DynamoDBAttribute(attributeName = "Seller")
    public String getSeller() {
        return seller;
    }

    public void setSeller(String seller) {
        this.seller = seller;
    }

    @DynamoDBAttribute(attributeName = "Item")
    public String getItemStack() {
        return itemStack;
    }

    public void setItemStack(String itemStack) {
        this.itemStack = itemStack;
    }

    @DynamoDBAttribute(attributeName = "Price")
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    @DynamoDBAttribute(attributeName = "Quantity")
    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    @DynamoDBAttribute(attributeName = "Stock")
    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getId() {
        return id;
    }

    public boolean isServer() {
        return server;
    }

    public Map<String, String> getValues() {
        Map<String, String> values = new HashMap<>();
        values.put("Seller", seller);
        values.put("Item", itemStack);
        values.put("Quantity", String.valueOf(quantity));
        values.put("Stock", String.valueOf(stock));
        values.put("Price", String.valueOf(price));
        values.put("ID", id);
        values.put("Server", String.valueOf(server));
        return values;
    }

    public void setServerListing(boolean b) {
        server = b;
    }
}
